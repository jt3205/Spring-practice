package sample2;

public class Foo {
	public Foo() {
		System.out.println("Foo 按眉 积己 : Foo() 龋免");
		System.out.println("---------------------");
	}
	public Foo(String str) {
		System.out.println("Foo 按眉 积己 : Foo(String) 龋免");
		System.out.println("傈崔等 String 按眉 : "+str);
		System.out.println("---------------------");
	}
}
